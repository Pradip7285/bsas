            <summary>How do I know BSAS parts will perform in harsh conditions?</summary>
            <div class="faq-ans">All components are designed and validated for specific applications and operating environments. We combine field feedback, OEM benchmarking, and engineering validation to ensure performance — not just fit.</div>
        </details>

        <details class="faq-item" data-cat="quality">
            <summary>Do you benchmark against OEM specifications?</summary>
            <div class="faq-ans">Yes. OEM benchmarking is a standard part of our engineering process. In many cases, we identify and correct design limitations that cause premature failure in Indian mining and construction conditions.</div>
        </details>

