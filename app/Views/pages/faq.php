            <summary>Can BSAS support full machine lifecycle management?</summary>
            <div class="faq-ans">Yes. From initial component supply through to scheduled overhauls, refurbishments, and end-of-life assessments, BSAS provides engineering-backed support across the complete machine lifecycle.</div>
        </details>

